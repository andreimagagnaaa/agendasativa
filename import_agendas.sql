SELECT 
    consultor,
    COUNT(*) as total_agendas,
    MIN(data_inicio) as primeira_agenda,
    MAX(data_fim) as ultima_agenda
FROM agendas
GROUP BY consultor
ORDER BY consultor;

-- Resumo geral
