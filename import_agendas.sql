    COUNT(*) as total_agendas,
    COUNT(DISTINCT consultor) as total_consultores,
    COUNT(DISTINCT projeto) as total_projetos,
    MIN(data_inicio) as periodo_inicio,
    MAX(data_fim) as periodo_fim
FROM agendas;
